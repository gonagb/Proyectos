// Declaramos una función integrando un parametro
function saluda(nombre,edad,email){
    console.log("Me llamo "+nombre);
    console.log("Tengo "+edad+" años");
    console.log("Mi correo es " +email);
}

// Llamamos al a función junto al parametro
saluda("Goncho",27,"goncho@gonagb.com");
saluda("JV",45,"info@jocarsa.com");
saluda("Teresa",25,"teresa@bocartdito.com");