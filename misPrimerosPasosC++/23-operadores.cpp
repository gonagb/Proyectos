#include <iostream>
using namespace std;


int main()
{
    // Variables 
    float operando1;
    float operando2;

    // Calculos 
    float suma = operando1 + operando2;
    float resta = operando1 - operando2;
    float multiplicacion = operando1 * operando2;
    float division = operando1 / operando2;


    // Salida de datos (out)
    cout << "El valor de la suma es :" << suma << "\n";
    cout << "El valor de la resta es :" << resta << "\n";
    cout << "El valor de la multiplicacion es :" << multiplicacion << "\n";
    cout << "El valor de la division es :" << division << "\n";


    return 0;
}
